public class Vardeclare {

    public void DeclareInt() {
        int integer = 5;
    }

    public int DeclareIntWithReturn() {
        int integer = 5;
        return integer;
    }

    public void DeclareIntWithParam(int a) {
        int integer = a;
    }

    public void DeclareBool() {
        boolean bool_ = true;
    }

    public boolean DeclareBoolWithReturn() {
        boolean bool_ = true;
        return bool_;
    }

    public void DeclareBoolWithParam(boolean a) {
        boolean bool_ = a;
    }

    public void DeclareString() {
        String string_ = "hej";
    }

    public String DeclareStringWithReturn() {
        String string_ = "hej";
        return string_;
    }

    public void DeclareStringWithParam(String a) {
        String string_ = a;
    }

}

