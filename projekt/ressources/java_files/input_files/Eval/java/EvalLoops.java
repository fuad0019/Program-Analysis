public class EvalLoops {
    public void loop1() {
        int a = 0;
        while (a < 5) {
            a = a + 1;
        }
    }

    public void loop2() {
        int a = 0;
        while (5 > a) {
            a = a + 1;
        }
    }

    public void loop3() {
        int a = 0;
        while (a == 0) {
            a = a + 1;
        }
    }

    public void loop4() {
        int a = 0;
        while (a != 0) {
            a = a + 1;
        }
    }
}
